<p style="text-align:center"><strong>L’ingénieur marocain au cœur du nouveau modèle de développement à l’ère de la digitalisation, de l’innovation et de l’accélération industrielle</strong></p>

<p>&nbsp;</p>

<p>Conscients de l'importance des stages dans la carrière des étudiants et de la valeur qu'ils ajoutent à l'édifice de leurs cursus, les élèves ingénieurs de deux grandes écoles d'ingénieurs : <strong>l'Ecole Nationale Supérieure d'Informatique et d'Analyse des Systèmes</strong> (<strong>ENSIAS</strong>) et <strong>l'Institut National de Statistique et d'Economie Appliquée</strong> (<strong>INSEA</strong>) se réunissent chaque année, dans le cadre du <strong>Forum GENI Entreprises</strong>, pour leur faciliter la tâche.</p>

<p>&nbsp;</p>

<p>Cette année, le <strong>Forum GENI Entreprises</strong> sera tenu <strong>le 23 et 24 Mars à L'ENSIAS</strong>, <strong>en partenariat avec ReKrute</strong>,&nbsp;dans le cadre de sa 18ème édition, sous le thème : “<strong>L’ingénieur marocain au cœur du nouveau modèle de développement à l’ère de la digitalisation, de l’innovation et de l’accélération industrielle</strong>". Le forum a pour finalité de jouer le rôle d'un pont entre les étudiants et le monde professionnel, de rapprocher les jeunes ambitieux, en recherche active de stages ou d'offres d'emploi, des entreprises et de les aider à décrocher les opportunités dont ils rêvent.</p>

<p>&nbsp;</p>

