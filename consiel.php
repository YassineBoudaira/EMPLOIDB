            <p></p><center><img src="https://www.rekrute.com/images/article-6228.png" style="width:70%"></center><p></p>

            <p>&nbsp;</p>

            <ul>
                <li><strong>La reprise du recrutement est réelle&nbsp;: un taux de croissance de 6%.</strong></li>
                <li><strong>Les centres d’appel et l’informatique restent les plus gros recruteurs.</strong></li>
                <li><strong>7/10 des profils demandés en informatique doivent être expérimentés. </strong></li>
                <li><strong>Une croissance de 13% des centres d’appel qui vivent une diversification vers les services numériques.</strong></li>
            </ul>

            <p>&nbsp;</p>

            <p>Avec la crise sanitaire qui a bouleversé le marché de l’emploi et <strong>le taux de turnover élevé </strong>qui marque le contexte actuel, les chercheurs d’emploi se posent de plus en plus de questions sur les tendances RH en termes de recrutement :<strong> les secteurs qui recrutent le plus, les métiers qui sont en forte demande et les profils les plus convoités par les entreprises.&nbsp;</strong></p>

            <p>ReKrute répond à ces questions à travers son <strong>baromètre annuel qui s’appuie sur l’analyse des offres d’emploi publiées en 2021</strong>. Un rendez-vous que les demandeurs d’emploi attendent impatiemment pour orienter leurs carrières.&nbsp;</p>

            <p>&nbsp;</p>

            <p><strong>Analyse Globale&nbsp;</strong></p>

            <p>2021 a été <strong>une année d’espoir</strong> pour le recrutement. La reprise s’est annoncée avec<strong> un taux de croissance de 6% par rapport à 2020.</strong> Le marché de l’emploi <strong>commence à retrouver son rythme d’avant Covid-19.</strong> Cela se confirme aussi par le fait que le pic du nombre d’annonces enregistré en juin frôle celui de février 2020 juste avant le confinement.</p>

            <p>&nbsp;</p>

            <p><strong>Les secteurs qui ont le plus recruté </strong></p>

            <p><strong>Les centres d’appel</strong> et <strong>l’informatique </strong>restent <strong>les plus gros recruteurs</strong> en 2021.</p>

            <p>Le secteur des <strong>centres d’appel </strong>a vu à la hausse son volume de recrutement, se positionnant ainsi <strong>en tête de liste des secteurs ayant le plus recruté.</strong></p>

            <p>Le secteur de<strong> l’informatique</strong>, quant à lui, occupe<strong> la 2ème place</strong> en générant <strong>12% des postes ouverts</strong> en 2021.</p>

            <p><strong>La banque</strong>, qui se positionne comme le secteur le plus attractif en 2021, a gardé le même rythme de recrutement par rapport à 2020, en offrant<strong> 4% de l’ensemble des postes ouverts</strong> aux chercheurs d’emploi.</p>

            <p>Lire aussi : <a href="https://www.rekrute.com/emplois-les-employeurs-les-plus-attractifs-au-maroc-en-2021-selon-rekrute-conseils-carriere-6219.html#.YjCr5XrMJPY">Les employeurs les plus attractifs au Maroc, en 2021.</a></p>

            <p>&nbsp;</p>

            <p><strong>Les fonctions en forte demande </strong></p>

            <p>En 2021, <strong>la fonction informatique</strong> représente <strong>1/3 des fonctions demandées</strong>, se plaçant ainsi<strong> en tête du classement,</strong> alors qu’elle occupait la 2ème place en 2020.</p>

            <p><strong>Les métiers du call center,</strong> passant <strong>en second</strong>, restent en <strong>très forte demande.</strong> Ces métiers représentent <strong>21%</strong> de la demande exprimée par les recruteurs en termes de fonctions.</p>

            <p>La nouveauté de l’année 2021 est <strong>l’arrivée des fonctions «&nbsp;Gestion/ Comptabilité/ Finance&nbsp;»</strong> et <strong>le retour de la fonction «&nbsp;Commercial / Vente&nbsp;» dans le top 5 du classement.</strong></p>

            <p>&nbsp;</p>

            <p><strong>Les profils les plus demandés ont quel niveau de formation et d’expérience&nbsp;? </strong></p>

            <p><strong>Les bac+5 </strong>sont les plus convoités par les recruteurs en 2021, représentant ainsi <strong>plus de la moitié des profils demandés.</strong> Les recruteurs comptent de manière significative sur les cadres expérimentés pour gérer la reprise économique du pays.</p>

            <p><strong>Les profils ayant entre 3 et 5 ans</strong> <strong>d’expérience </strong>occupent la première place parmi les niveaux d’expérience demandés, avec un pourcentage de<strong> 28%</strong>, suivis des profils ayant <strong>entre 1 et 3 ans d’expérience</strong> avec <strong>24% </strong>et des séniors <strong>ayant de 5 à 10 ans avec 23%</strong>. Quant à eux, les débutants ne représentent que <strong>16% </strong>de l’ensemble de la demande contrairement à 2020, où ils dominaient le classement.</p>

            <p>&nbsp;</p>

            <p><strong>Zoom sur le secteur des centres d’appel </strong></p>

            <p>En 2021, le secteur des<strong> centres d’appel </strong>a enregistré une <strong>croissance de 13%,</strong> en passant de <strong>50883 postes</strong> ouverts en<strong> 2020</strong> à <strong>57360 </strong>en <strong>2021</strong>. Et ils font de plus en plus appel à des <strong>profils informaticiens.</strong></p>

            <p>Cette année, le secteur a recruté plutôt<strong> des profils expérimentés (3 à 5 ans d’expérience),</strong> au détriment des débutants qui ne représentent plus que 23% de l’ensemble des profils demandés. En 2020, c’était la catégorie la plus demandée.<strong> </strong></p>

            <p>&nbsp;</p>

            <p><strong>Zoom sur le secteur de l’informatique</strong></p>

            <p>Le secteur de <strong>l’informatique </strong>affiche <strong>une baisse du nombre de postes proposés</strong>, de 13774 postes en 2020 à 9411 postes en 2021. Le Covid-19 aurait-il diminué le taux de turnover de cette population&nbsp;?</p>

            <p>Comme chaque année, <strong>la fonction «&nbsp;Informatique / Électronique&nbsp;</strong>» <strong>domine le classement </strong>des fonctions les plus demandées du secteur, avec un pourcentage de <strong>84%.</strong>&nbsp; La fonction <strong>Télécoms / Réseaux,</strong> en deuxième position, représente<strong> 3% </strong>de l’ensemble des fonctions demandées. Cependant, <strong>la fonction RH / Personnel / Formation</strong> a fortement intéressé les recruteurs en 2021, puisqu’elle occupe <strong>la 3ème place du podium.</strong></p>

            <p><strong>L’informatique demeure un secteur friand des cadres séniors. 7 profils demandés sur 10 doivent avoir de 3 à 10 ans d’expérience. </strong>Les profils juniors (moins de 3 ans d’expérience) ne représentent que le quart des profils recherchés. Quant au niveau d’études, <strong>la catégorie Bac+5 est la plus demandée avec un pourcentage de 77%, </strong>suivie par les bac+3 et les bac+4 avec 16% de l’ensemble des profils IT et en 3ème place viennent les candidats n’ayant pas dépassé bac+2.</p>
