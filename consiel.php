            <p></p><center><img src="https://www.rekrute.com/images/article-6229.png" width="70%"></center><p></p>

            <p>&nbsp;</p><p>Généralement un entretien se termine par la question&nbsp; "Avez-vous des questions ?". C'est donc votre chance de découvrir le maximum d'informations utiles sur le poste et l'employeur, et de marquer des points auprès de lui. Évitez, alors, de poser des questions sur le salaire et faites preuve de curiosité et de sérieux.</p>

            <p>Voici quelques suggestions :</p>

            <p>Lire aussi :<a href="https://www.rekrute.com/emplois-kapacity-revealer-la-reponse-a-pourquoi-vous-conseils-carriere-6217.html#.YjCrW3rMJPY">&nbsp;Kapacity Revealer, la réponse à " Pourquoi vous ? "</a></p>

            <p><strong>1. Comment cette ouverture de poste a eu lieu ?</strong></p>

            <p>Cette question vous permettra de comprendre <strong>l'importance et l'historique du poste </strong>: combien de personnes l'ont occupé avant vous et pourquoi il est vacant maintenant (saisonnalité, démission&nbsp;de l'employé précédent, etc). La façon dont l'employeur répond à la question en dira long sur la rotation du personnel dans l'entreprise.</p>

            <p>Si le poste vient d’être créé, il est important de bien comprendre <strong>le besoin du recruteur</strong>. Si ce n'est pas le cas, vous pouvez lui demander&nbsp;de vous donner plus d’information sur les forces et les lacunes de la personne qui occupait le poste avant vous.</p>

            <p>&nbsp;</p>

            <p><strong>2. Quelles sont les difficultés qu'il se peut que je rencontre&nbsp;?</strong></p>

            <p>Cette question vous aidera à découvrir<strong> les difficultés </strong>ou bien <strong>les challenges</strong>&nbsp;de votre futur travail. En d'autres termes, découvrez ce qui est arrivé au prédécesseur, s'il y en avait un, ce que vous devriez éviter et ce que vous pouvez améliorer. Cela vous aidera à avoir plus de confiance face aux problèmes.</p>

            <p>&nbsp;</p>

            <p><strong>3. À quoi ressemblera ma journée type au travail ?</strong></p>

            <p>Donnez à l’interview un <strong>nouveau souffle</strong> avec cette question simple, mais pragmatique.&nbsp;</p>

            <p>&nbsp;</p>

            <p><strong>4. Selon quels critères mon travail sera-t-il évalué ?</strong></p>

            <p>Si vous souhaitez en savoir plus sur <strong>les priorités du poste </strong>et de vos éventuels supérieurs, cette question vous apportera quelques éclaircissements.</p>

            <p>&nbsp;</p>

            <p><strong>5. Pourquoi les employés aiment-ils travailler ici ?</strong></p>

            <p>Une manière intelligente d'apprendre <strong>les valeurs de l'entreprise</strong>, son éthique et sa culture d'entreprise.</p>

            <p>&nbsp;</p>

            <p><strong>6. Qui sont les personnes qui réussissent&nbsp;dans l'entreprise et pourquoi ?</strong></p>

            <p>Observez bien le ton de votre interlocuteur. Si l'ambiance de travail est saine, <strong>les meilleurs joueurs</strong> seront mentionnés avec enthousiasme.</p>

