        <h1>Tendances RH en 2021 : quels sont les profils les plus convoités par les recruteurs selon ReKrute ? </h1>
    </div>

    <div class="post-info">
        <em class="date">14 Mars 2022</em>
                <span class="visitors"> Lu par 1028 personne(s) </span> 
    </div>

    <div class="social-plugin">
